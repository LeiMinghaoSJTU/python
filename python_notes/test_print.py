# 定义不同类型变量
a = 42            # 整数
b = 3.14159       # 浮点数
c = "Hello"       # 字符串
d = True          # 布尔值
e = [1, 2, 3]     # 列表
f = (4, 5, 6)     # 元组
g = {"name": "Alice", "age": 30}  # 字典
h = 3 + 4j        # 复数
i = None          # None

# 使用f-string格式化输出
print(f"整型变量a的值是{a}")     
print(f"浮点型变量b的值是{b}")
print(f"字符串型变量c的值是{c}")
print(f"布尔型变量d的值是{d}")
print(f"列表型变量e的值是{e}")
print(f"元组型变量f的值是{f}")
print(f"字典型变量g的值是{g}")
print(f"复数型变量h的值是{h}")
print(f"None型变量i的值是{i}")